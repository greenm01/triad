proc bitmapTextWidth*(text: string): int32 =
  int32(text.len) * 6'i32 * BitmapScale

proc bitmapTextHeight*(): int32 =
  7'i32 * BitmapScale

proc drawBitmapChar(
    buf: var PixelBuffer, x, y: int32, ch: char, color: uint32, scale = BitmapScale
) =
  let glyph = glyphRows(ch.toUpperAscii())
  for gy, row in glyph:
    for gx, bit in row:
      if bit == '1':
        buf.fillRect(x + int32(gx) * scale, y + int32(gy) * scale, scale, scale, color)

proc drawBitmapText*(
    buf: var PixelBuffer, x, y, maxW: int32, text: string, color: uint32
) =
  var dx = x
  let advance = 6'i32 * BitmapScale
  for ch in text:
    if dx + 5'i32 * BitmapScale > x + maxW:
      return
    buf.drawBitmapChar(dx, y, ch, color)
    dx += advance
